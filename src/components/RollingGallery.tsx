const RollingGallery = ({ autoplay = false, pauseOnHover = false, images = [] }) => {
  const { isDarkTheme } = useTheme();
  const [isClient, setIsClient] = useState(false);
  const [isScreenSizeSm, setIsScreenSizeSm] = useState(false);
  
  // Initialize the state safely for SSR
  useEffect(() => {
    setIsClient(true);
    setIsScreenSizeSm(window.innerWidth <= 640);
  }, []);

  // Default to provided images or fallback to IMGS
  const displayImages = images.length > 0 ? images : IMGS;
  
  // Only set these values on the client side
  const cylinderWidth = isClient ? (isScreenSizeSm ? 1100 : 1800) : 1800;
  const faceCount = displayImages.length;
  const faceWidth = (cylinderWidth / faceCount) * 1.5; // Increased width for items
  const dragFactor = 0.05;
  const radius = cylinderWidth / (2 * Math.PI);

  const rotation = useMotionValue(0);
  const controls = useAnimation();
  const autoplayRef = useRef();

  const handleDrag = (_, info) => {
    rotation.set(rotation.get() + info.offset.x * dragFactor);
  };

  const handleDragEnd = (_, info) => {
    controls.start({
      rotateY: rotation.get() + info.velocity.x * dragFactor,
      transition: { type: "spring", stiffness: 60, damping: 20, mass: 0.1, ease: "easeOut" },
    });
  };

  const transform = useTransform(rotation, (value) => {
    return `rotate3d(0, 1, 0, ${value}deg)`;
  });

  // Autoplay effect with adjusted timing
  useEffect(() => {
    if (isClient && autoplay) {
      autoplayRef.current = setInterval(() => {
        controls.start({
          rotateY: rotation.get() - (360 / faceCount),
          transition: { duration: 2, ease: "linear" },
        });
        rotation.set(rotation.get() - (360 / faceCount));
      }, 2000);

      return () => clearInterval(autoplayRef.current);
    }
  }, [isClient, autoplay, rotation, controls, faceCount]);

  useEffect(() => {
    if (isClient) {
      const handleResize = () => {
        setIsScreenSizeSm(window.innerWidth <= 640);
      };

      window.addEventListener("resize", handleResize);
      return () => window.removeEventListener("resize", handleResize);
    }
  }, [isClient]);

  // Pause on hover with smooth transition
  const handleMouseEnter = () => {
    if (autoplay && pauseOnHover && isClient) {
      clearInterval(autoplayRef.current);
      controls.stop(); // Stop the animation smoothly
    }
  };

  const handleMouseLeave = () => {
    if (autoplay && pauseOnHover && isClient) {
      controls.start({
        rotateY: rotation.get() - (360 / faceCount),
        transition: { duration: 2, ease: "linear" },
      });
      rotation.set(rotation.get() - (360 / faceCount));

      autoplayRef.current = setInterval(() => {
        controls.start({
          rotateY: rotation.get() - (360 / faceCount),
          transition: { duration: 2, ease: "linear" },
        });
        rotation.set(rotation.get() - (360 / faceCount));
      }, 2000);
    }
  };

  if (!isClient) {
    return <div className="w-full h-[300px] bg-gray-200 animate-pulse rounded-lg"></div>;
  }

  return (
    <div className={`gallery-container ${isDarkTheme ? 'dark-theme' : 'light-theme'}`}>
      <div className={`gallery-gradient gallery-gradient-left ${isDarkTheme ? '' : 'light-gradient-left'}`}></div>
      <div className={`gallery-gradient gallery-gradient-right ${isDarkTheme ? '' : 'light-gradient-right'}`}></div>
      <div className="gallery-content">
        <motion.div
          drag="x"
          className="gallery-track"
          onMouseEnter={handleMouseEnter} 
          onMouseLeave={handleMouseLeave}
          style={{
            transform: transform,
            rotateY: rotation,
            width: cylinderWidth,
            transformStyle: "preserve-3d",
          }}
          onDrag={handleDrag}
          onDragEnd={handleDragEnd}
          animate={controls}
        >
          {displayImages.map((url, i) => (
            <div
              key={i}
              className="gallery-item"
              style={{
                width: `${faceWidth}px`,
                transform: `rotateY(${i * (360 / faceCount)}deg) translateZ(${radius}px)`,
              }}
            >
              <img src={url} alt="gallery" className={`gallery-img ${isDarkTheme ? '' : 'light-border'}`} />
            </div>
          ))}
        </motion.div>
      </div>
    </div>
  );
};

export default RollingGallery; 