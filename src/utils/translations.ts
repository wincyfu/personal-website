// 根据当前语言获取文本的辅助函数
export function getTranslatedText(path: string, isEnglish: boolean): string {
  const keys = path.split('.');
  let value: any = translations;

  for (const key of keys) {
    if (value && value[key]) {
      value = value[key];
    } else {
      return path; // 返回路径作为后备
    }
  }

  return value[isEnglish ? 'en' : 'zh'] || path;
} 